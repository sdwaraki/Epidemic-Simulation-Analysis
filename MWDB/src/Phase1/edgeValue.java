/**
 * 
 * An intermediate object to hold a value and its index
 */

package Phase1;

/**
 * 
 * A temporary object which contains the max and min value and their corresponding index
 * 
 *
 */
public class edgeValue 
{
	double value;
	int index;
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	
}
